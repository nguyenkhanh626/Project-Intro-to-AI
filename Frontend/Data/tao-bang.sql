CREATE DATABASE INTRO_AI;
USE INTRO_AI;

CREATE TABLE STATIONS (
STATION_ID varchar(30) PRIMARY KEY,
STATION_NAME VARCHAR(255) NOT NULL,
LATITUDE DECIMAL(10, 8),
LONGITUDE DECIMAL(10, 8),
COORDINATES POINT SRID 4326
);

CREATE TABLE LINE (
LINE_ID varchar(30) PRIMARY KEY,
LINE_NAME VARCHAR(255) NOT NULL,
COLOR VARCHAR(20) NOT NULL,
TYPEE VARCHAR(30) NOT NULL
);

CREATE TABLE STATION_LINE (
STATION_ID varchar(30) NOT NULL,
LINE_ID varchar(30) NOT NULL,
STOP_SEQUENCE INT NOT NULL,
PRIMARY KEY (STATION_ID,LINE_ID),
FOREIGN KEY (STATION_ID) REFERENCES STATIONS(STATION_ID),
FOREIGN KEY (LINE_ID) REFERENCES LINE(LINE_ID)
);

CREATE TABLE stops_raw (
stop_id VARCHAR(20),
stop_name VARCHAR(255),
stop_lat DECIMAL(12,8),
stop_lon DECIMAL(12,8), 
zone_id VARCHAR(10),
wheelchair_boarding VARCHAR(10)
);

